int	mode(int m)
{
	static int	mo;

	if (m > -1)
		mo = m;
	return (mo);
}

int	level(int m)
{
	static int	mo;

	if (m == 0)
		mo = 0;
	else if (mo == 1 || mo == -1)
		mo += m;
	return (mo);
}
